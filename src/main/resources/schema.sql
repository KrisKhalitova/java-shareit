CREATE TABLE IF NOT EXISTS users (
    user_id LONG GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    name VARCHAR(128) NOT NULL,
    email VARCHAR(64) NOT NULL,
    CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS items (
    item_id LONG GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    name VARCHAR(128) NOT NULL,
    description VARCHAR(256) NOT NULL,
    is_available BOOLEAN NOT NULL,
    owner_id LONG NOT NULL,
    request_id LONG NOT NULL
);

CREATE TABLE IF NOT EXISTS bookings (
    booking_id LONG GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    start_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    end_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    item_id LONG REFERENCES items (item_id) ON DELETE CASCADE,
    booker_id LONG REFERENCES users (user_id) ON DELETE CASCADE,
    status VARCHAR NOT NULL
);

CREATE TABLE IF NOT EXISTS comments (
    comment_id LONG GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    text VARCHAR NOT NULL,
    item_id LONG REFERENCES items (item_id) ON DELETE CASCADE,
    author_id LONG REFERENCES users (user_id) ON DELETE CASCADE
);